import 'package:dio/dio.dart';
import 'package:cbnu_restaurant/model/markerdata.dart';

class GetMarker {
  getHttp(url) async {
    try {
      var response =
          await Dio().get(url);
      List<Markerdata> markerdata = (response.data)
          .map<Markerdata>((json) => Markerdata.fromJson(json))
          .toList();
      return markerdata;
    } catch (e) {
      print(e);
      print('???????????????????????????????????????????????????');
      throw Exception();
    }
  }
}


